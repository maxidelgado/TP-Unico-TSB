    private void setModel(){
        modelo = new DefaultTableModel();
        
        columnas = new String[4];
        columnas[0] = "ID";
        columnas[1] = "PALABRA";
        columnas[2] = "CANTIDAD";
        columnas[3] = "LIBROS";
        
        tbPalabras.setModel(modelo);
        modelo.setDataVector(new ControladorDB().getPalabras(), columnas);
        tbPalabras.updateUI();
    }
    
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        tareas.clear();
        JFileChooser selector = new JFileChooser();
        selector.setMultiSelectionEnabled(true);
        
        //FileFilter es usado como una clase an√≥nima
        selector.setFileFilter(new javax.swing.filechooser.FileFilter() {
            @Override
            public boolean accept(File f) {
                return (f.isFile() && f.getName().endsWith(".txt")) || f.isDirectory();
            }

            @Override
            public String getDescription() {
                return "Archivos de texto plano";
            }
        });
        if (selector.showDialog(this, "Abrir") != JFileChooser.CANCEL_OPTION) {
            File[] file = selector.getSelectedFiles();

            for (File f : file) {
                tareas.add(f);
                if (!jtaCola.getText().isEmpty()) {
                    jtaCola.setText(jtaCola.getText() + '\n' + f.getName());
                } else {
                    jtaCola.setText(f.getName());
                }
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        for (File file : tareas) {
            new ControladorDB(file).guardar_libro();
        }
        JOptionPane.showMessageDialog(this, "Las palabras se agregaron exitosamente.", "Cola procesada", 1);
        //modelo.setDataVector(new ControladorDB().getPalabras(), columnas);
        //tbPalabras.updateUI();
        tareas.clear();
    }//GEN-LAST:event_btnCargarActionPerformed


