    private class TableModelPalabras extends AbstractTableModel
    {

        //Esto funciona rapid√≠simo y anda en los 50 megas de ram
        ArrayList arr = db.toArray();

        @Override
        public int getRowCount()
          {
            return arr.size();
          }

        @Override
        public int getColumnCount()
          {
            return 2;
          }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex)
          {
            try
              {
                Pair p = (Pair) arr.get(rowIndex);
                switch (columnIndex)
                  {

                    case 0:

                        return p.getKey();
                    case 1:

                        return p.getValue();
                  }

              } catch (Exception e)
              {
                System.err.println("No se pudo crear la tabla" + e.getMessage());
              }
            return 0;
          }

        @Override
        public String getColumnName(int column)
          {
            String[] cols =
              {
                "Palabra", "Frecuencia"
              };
            return cols[column];
          }
    }

